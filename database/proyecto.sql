--  DDL for Procedure PRC_RESUMEN_VENTA
--------------------------------------------------------
set define off;

  CREATE OR REPLACE EDITIONABLE PROCEDURE "MAURICIO"."PRC_RESUMEN_VENTA" (p_id_venta NUMBER)
IS
    v_existe NUMBER;
    v_total  NUMBER;
BEGIN
    -- Verifica si la venta existe
    SELECT COUNT(*)
    INTO v_existe
    FROM UL_EE2_VENTA
    WHERE ID_VENTA = p_id_venta;

    IF v_existe = 0 THEN
        DBMS_OUTPUT.PUT_LINE('Venta no encontrada');
    ELSE
        -- Llama a la función para calcular el total
        v_total := FN_TOTAL_VENTA(p_id_venta);

        DBMS_OUTPUT.PUT_LINE('Resumen de Venta');
        DBMS_OUTPUT.PUT_LINE('ID_VENTA: ' || p_id_venta);
        DBMS_OUTPUT.PUT_LINE('MONTO_TOTAL: ' || v_total);
    END IF;
END;

/
--------------------------------------------------------
--  DDL for Function FN_TOTAL_VENTA
--------------------------------------------------------

  CREATE OR REPLACE EDITIONABLE FUNCTION "MAURICIO"."FN_TOTAL_VENTA" (p_id_venta NUMBER)
RETURN NUMBER
IS
    v_total NUMBER := 0;
BEGIN
    -- Calcula el total de la venta
    SELECT NVL(SUM(vd.CANTIDAD * vd.PRECIO_UNIT), 0)
    INTO v_total
    FROM UL_EE2_VENTA_DETALLE vd
    WHERE vd.ID_VENTA = p_id_venta;

    RETURN v_total;
END;

